#import "LoadingView.h"

@implementation LoadingView

- (void)resetFrameViewShow:(UIView *)viewShow  vcShow:(UIViewController *)vcShow{
    CGRect frame_loadingView;
    
    if (viewShow == nil) {
        frame_loadingView = [UIScreen mainScreen].bounds;
    }else{
        frame_loadingView = viewShow.frame;
    }
    self.frame = frame_loadingView;
}




@end
